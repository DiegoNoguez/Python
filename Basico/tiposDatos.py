#TIPOS DE DATOS existentes en python 

#tipo entero
numero=50
print(numero)
print(type(numero))

#tipo float o punto flotante
#ejemplo 
numero2=2.5
print("Lo siguiente es un numero de tipo de dato float: \n",numero2)
print(type(numero2))

#tipo string o cadenas de caracteres
cadena='hola'
print(cadena)
print(type(cadena))

#tipo de datos BOOLEANOS 
booleano=True 
print(booleano)
print(type(booleano))

##estos son la mayoria de los tipos de datos en python 